package org.jhotdraw.geom;

/**
 *
 * @author Alex
 */
public class GeomPointInt {
    private int x;
    private int y;
    
    public GeomPointInt(int x, int y){
        this.x = x;
        this.y = y;
    }

    /**
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * @return the y
     */
    public int getY() {
        return y;
    }
}
